          <p class="article-words">Artists Ed Bader and Peter Greendale explore  Alberta in their  exhibit “Northwest/Southwest” which opened at Casa, Nov. 4. “Both of them are established artists who earned BFAs  at the University of Lethbridge before beginning their art careers,” said Casa curator Darcy Logan.</p>
          <blockquote>“It is an exploration of Alberta and two different geographic areas,’” Logan continued.</blockquote>
          <blockquote>“ Ed Bader uses colours and collages to describe northwestern Alberta. While Peter Greendale uses photographs to describe southwestern Alberta,” Logan said.</blockquote>
          <p>The second half of the main gallery features something  completely different— Ethan Roth’ s</p>
          <p>“Faces and Facades, which includes full sized paper sculptures of familiar cosplay and pop culture uniforms and armour.</p>
          <blockquote>“They are large folded paper sculptures which reflects his love of cosplay,” Logan observed.</blockquote>
          <blockquote>“There are five large paper sculptures.”</blockquote>
          <p>One familiar sculptures include a Star Wars storm trooper and Iron Man. The others come from Graphic novels and video games.</p>
          <blockquote>“ There are also  two completed ones inspired by the virtual reality game Skyrim he has taken to conventions across the country,” Logan said.</blockquote>
          <p>To indicate how much work goes into each costumes, there is a breakdown of a seemingly simple cube, which includes several thousand individual components.There are three other exhibitions  at Casa.</p>
          <p>Len Komenac, whose comic strip/ Roy Lichtenstein/Andy Warhol  inspired work is often on display at the Owl Acoustic lounge tries something a little different for his exhibit “Small Worlds,” which is on display in the Casa entrance hall.</p>
          <blockquote>“He’s forced alternate perception to create miniature worlds he has photographed,” Logan said.</blockquote>
          <p>There are also two exhibits upstairs. Linda Hajash’s “Not to be Toyed With” explores how toys affect genre roles in children.</p>
          <blockquote>“So she’s altered Barbie dolls to deal with genre roles,” Logan said.</blockquote>
          <blockquote>The last exhibit is Criss Wiebe’s exhibit “ The Nature of Transition.”</blockquote>
